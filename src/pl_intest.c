#include "fillit.h"

int		pl_intest(char *grid, int x, int *nbr)
{
	if (grid == NULL && x == -1)
	{
		*nbr = 0;
		return (1);
	}
	if (grid == NULL)
	{
		*nbr = *nbr - 1;
		return (1);
	}
	return (0);
}
